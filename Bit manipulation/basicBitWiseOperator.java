import java.util.*;
public class basicBitWiseOperator{ 
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println( ~10 );
        System.out.println(5 << 2);
        System.out.println(6 >> 1);
    }
}