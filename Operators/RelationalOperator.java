import java.util.*;
public class RelationalOperator{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int a = 10;
        int b = 10;
        System.out.println(a == b); // Equal to operator
        System.out.println(a != b); // Not  equal to operator
        System.out.println(a > b); // Greater than operator
        System.out.println(a < b); // Less than operator
        System.out.println(a >= b); // Greater than equal to operator
        System.out.println(a <= b); // Less than equal to operator
    
    }
}