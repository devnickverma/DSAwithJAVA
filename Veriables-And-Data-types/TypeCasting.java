import java.util.*;
public class TypeCasting{
    public static void main (String args[]){
        // Conveting one data type into another small data type
        // Type Casting manually 
        // Explicit conversion
        // Narroing conversion
        float a = 25.5f;
        int b = (int) a;
        System.out.println(b);

        char ch = 'a';
        int num = ch;
        System.out.print(num);
    }
}